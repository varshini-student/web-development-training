// map
let nums = [1, 2, 3];
console.log(nums.map(n => n + 1));

// filter
let marks = [35, 60, 80];
console.log(marks.filter(m => m >= 50));

// reduce
let bills = [100, 200, 300];
console.log(bills.reduce((t, b) => t + b, 0));

// JSON object
let student = { name: "Varshini", age: 20 };
console.log(student.name);